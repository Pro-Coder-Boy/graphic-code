from turtle import*
def write_text(x, y, size, c , text):
    penup()
    goto(x,y)
    pendown()
    color(c)
    style = ('b yekan', size, 'bold')
    write(text, font=style, align='center')
def circle_draw(x,y,r,l_w,l_c,f_c):
    penup()
    goto(x,y)
    pendown()
    pensize(l_w)
    color(l_c)
    fillcolor(f_c)
    begin_fill()
    circle(r)
    end_fill()
def square(x,y,w,h,l_w,f_c,l_c):
    penup()
    goto(x,y)
    pendown()
    pensize(l_w)
    color(l_c)
    fillcolor(f_c)
    begin_fill()
    forward(w)
    left(90)
    forward(h)
    left(90)
    forward(w)
    left(90)
    forward(h)
    end_fill()
    left(90)
